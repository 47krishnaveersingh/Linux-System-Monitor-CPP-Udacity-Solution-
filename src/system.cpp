Processor& System::Cpu() { return cpu_; }

vector<Process>& System::Processes() {
  vector<Process> unsort;
  vector<int> pids = LinuxParser::Pids();
  for (int i : pids) {
    Process p(i);
    unsort.push_back(p);
  }

  sort(unsort.begin(), unsort.end(), [](Process& a, Process& b) {
    return (b.CpuUtilization() < a.CpuUtilization());
  });

  processes_ = unsort;
  return processes_;
}

